# print numbers from 0 to 4
for i in range(5):
    print(i)
# output - 0 1 2 3 4

# print numbers from 1 to 5
for i in range(1, 6):
    print(i)
# output - 1 2 3 4 5

# print numbers from 1 to 9 with step size 2
for i in range(1, 10, 2):
    print(i)
# output - 1 3 5 7 9

# print each letter of the string
for letter in "Saksham":
    print(letter)
# output - S a k s h a m
